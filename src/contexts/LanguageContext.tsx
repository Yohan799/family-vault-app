export const LanguageProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<Language>(() => {
    // Use sync method for initial render
    return languageStorage.getSync() as Language || "en";
  });

  // Load language from storage on mount (for APK persistence)
  useEffect(() => {
    const loadLanguage = async () => {
      const saved = await languageStorage.get();
      if (saved && saved !== language) {
        setLanguageState(saved as Language);
      }
    };
    loadLanguage();
  }, []); // Empty dependency array is correct - only run on mount

  useEffect(() => {
    // Update document lang attribute for accessibility
    document.documentElement.lang = language;
  }, [language]);

  const setLanguage = useCallback(async (lang: Language) => {
    setLanguageState(lang);
    await languageStorage.set(lang);
  }, []);

  const t = useCallback((key: string, replacements?: Record<string, string | number>): string => {
    let text = translations[language][key] || translations.en[key] || key;

    // Handle replacements like {count}
    if (replacements) {
      Object.entries(replacements).forEach(([k, v]) => {
        text = text.replace(`{${k}}`, String(v));
      });
    }

    return text;
  }, [language]); // Re-create when language changes

  const currentLanguage = useMemo(() => 
    languages.find((l) => l.code === language) || languages[0],
    [language]
  );

  const contextValue = useMemo(() => ({
    language,
    setLanguage,
    t,
    currentLanguage
  }), [language, setLanguage, t, currentLanguage]);

  return (
    <LanguageContext.Provider value={contextValue}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = (): LanguageContextType => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used within a LanguageProvider");
  }
  return context;
};
